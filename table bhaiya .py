# table generator(babu chandu bhaiya) 
def table_generator():
     num= int(input("Enter a number: "))
     
     start = int(input("Enter a Start: "))
     
     end = int(input("Enter a Last: "))
     
     for i in range(start, end + 1):
         print(f"{num} x {i} = {num * i}")
    


